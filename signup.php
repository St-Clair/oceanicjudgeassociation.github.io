                                            <article class="box post">
                                                <header>
												    <h3><strong>1: Create a Judge Apps Account</strong></h3>
									            </header>
											    <p>Register for an account on Judge Apps to access the Judge Forums and Resources.<br>
                                                Your Judge Apps account is also used to uniquely identify you when working.
                                                </p>
                                                <a href="https://apps.magicjudges.org/" class="button icon solid"><img src="images/external.svg"> Judge Apps</a>
										    </article>
                                        </li>
                                        <li>
                                            <article class="box post">
                                                <header>
												    <h3><strong>2: Learn Rules and Policy</strong></h3>
									            </header>
											    <p>Begin studying the <a href='https://mtgdoc.link/cr'><strong>Magic: The Gathering Comprehensive Rules</strong></a>, <a href='https://mtgdoc.link/mtr'><strong>Magic Tournament Rules (MTR)</strong></a>, and <a href='https://mtgdoc.link/jar'><strong>Judging at Regular (JAR)</strong></a> documents.<br>
                                                While you can study independently using the resources at the bottom of the page, working with an L2+ Judge mentor is highly recommended.<br>
                                                A mentor can guide you through complex rules interactions, share practical judging experience, help you prepare for certification, and answer questions as they arise.<br>
                                                Many aspiring judges find that having someone to discuss scenarios and review practice questions with significantly accelerates their learning and confidence.<br>
                                                If you have difficulty finding a mentor, you can reach out to OJA for a recommendation.
                                                </p>
										    </article>
                                        </li>
                                        <li>
                                            <article class="box post">
                                                <header>
												    <h3><strong>3: Gain Experience & Judge Your First Events</strong></h3>
									            </header>
											    <p>Once you feel ready, ask to judge an event at your Local Game Store (LGS). Great starting points include:
                                                    <ol>
                                                        Friday Night Magic (FNM)<br>
                                                        Standard Showdown<br>
                                                        Commander Night<br>
                                                        Draft events<br>
                                                        Prerelease tournaments<br>
                                                    </ol>
                                                </p>
										    </article>
                                        </li>
                                        <li>
                                            <article class="box post">
                                                <header>
												    <h3><strong>4: Join the Oceanic Judge Association (OJA)</strong></h3>
									            </header>
											    <p>Fill out the <a href="https://docs.google.com/forms/d/e/1FAIpQLSeqV0oj1hN-hwULZglt91ybLxCDd3ZBdn1cy5HFU4ZKbC0gUw/viewform?usp=sf_link"><strong>OJA Application Form</strong></a>, and pay the annual membership dues of $25 AUD.</p>
										    </article>
                                        </li>
                                        <li>
                                            <article class="box post">
                                                <header>
												    <h3><strong>5: Take the L1 Rules and Policy Test</strong></h3>
									            </header>
											    <p>When you and your mentor feel you're ready, ask your mentor to create the L1 Rules and Policy test for you on Judge Apps.<br>
                                                To help study for the test you can use the <a href="https://www.judgefoundry.org/learn-more/exam-study-guides/level-one-rules-and-policy-exam-study-guide/"><strong>Level 1 Rules and Policy Exam Study Guide</strong></a><br><br>
                                                <strong>If you don't pass: Don't worry! You can debrief with your mentor to understand areas for improvement, and retake the test after a cooldown period when you're ready.</strong>
										    </article>
                                        </li>
                                        <li>
                                            <article class="box post">
                                                <header>
												    <h3><strong>6: Complete the Event Management Competency (EM1)</strong></h3>
									            </header>
											    <p>Ask your mentor or another local Judge to observe you judging an event. They will endorse you for your EM1 Competency via an Advancement Review on Judge Apps.<br>
                                                You must have judged at least one limited event (Prerelease, Drafts) AND one constructed event (FNM, Standard Showdown)<br>
                                                The review must include:
                                                    <ol>
                                                        Evaluation of your abilities to run the event<br>
                                                        Evaluation of your abilities to use event software (EventLink)<br>
                                                        Evaluation of your ability to distribute product<br>
                                                        Evaluation of your abilities to take judge calls<br>
                                                        Confirmation that your values and maturity meet OJA standards
                                                    </ol>
                                                </p>
										    </article>
                                        </li>
                                        <li>
                                            <article class="box post">
                                                <header>
												    <h3><strong>You're now a Judge!</strong></h3>
									            </header>
											    <p>With your L1 test completed, EM1 endorsement, and paid OJA dues, you are now an L1 Judge!<br>
                                                <strong>Welcome to the fold!</strong></p>
