												    <h3><strong>Pre-requisites</strong></h3>
									            </header>
											    <p>Becoming an accredited Judge requires you to work at least 1 event with an L2 or higher Judge, who will mentor you on your learning journey.<br>
                                                If you live remote this requirement can be waived at the discretion of your mentor in communication with other high level Judges.<br>
                                                A nominal fee and internet access is also required.
                                                </p>
                                                <h3>If you can meet these requirements continue below to start your Judge journey!</h3>
										    </article>
                                        </li>
